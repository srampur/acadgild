/**
 * Write  a  program  to  print  the  sum  of  two  numbers. 
 * One  number  should  be  int  and  another  should  be float
 * Output: The sum of two numbers should be in integer.
 */
public class Add
{
	public static void main(String args[])
	{
		int iNum = 10;
		float fNum = 11.4f;
		int sum = (int) (iNum + fNum);
		System.out.println("Sum of two number in integer is "+sum);
	}
}
